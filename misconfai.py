# match
def match_rule(content, rule):
    matches = []
    for key in rule.get('keys', []):
        if key in content:
            value = str(content[key])
            if re.search(rule.get('value_regex', '.*'), value):
                matches.append({
                    "rule_id": rule["id"],
                    "cwe": rule["cwe"],
                    "description": rule["description"],
                    "severity": rule["severity"],
                    "matched_key": key,
                    "matched_value": value
                })
    return matches

